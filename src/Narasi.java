public class Narasi{
  private String awal;
  private String akhir;

  public void setAwalNarasi(String vAwal){
    this.awal = vAwal;
  }

  public void setAkhirNarasi(String vAkhir){
    this.akhir = vAkhir;
  }

  public String getAwalNarasi(){
    return awal;
  }

  public String getAkhirNarasi(){
    return akhir;
  }
}